# Print multiple lines
print("A")
print("B")
print("C")

# Leaves the whole line at the end of each print function
# because default end line character is end line character ("/n")
# LINE 1 --> print("A" end='/n;)

# To change the end line character
# e.g. changing end line character from '/n' --> ' '

print("A", end=' ')
print("B")