# Comparison Operators --> <, <=, >, >=
#                          ==, !=

# <
print(10<20)                            # True
print(10<5)                             # False
print(10<10)                            # False

# >
print(10>20)                            # False
print(10>5)                             # True
print(10>10)                            # False

# <=
print(10<=20)                            # True
print(10<=5)                             # False
print(10<=10)                            # True

# >=
print(10>=20)                            # False
print(10>=5)                             # True
print(10>=10)                            # True

# ==
print(10==20)                            # False
print(10==10)                            # True
print("10"==10)                          # False (Check both value and datatype)

# != (Not equals to)
print(10!=20)                            # True
print(10!=10)                            # False
print("10"!=10)                          # True