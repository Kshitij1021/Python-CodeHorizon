# Assignment Operators --> =, +=, -=, *=, /=, %=, //=, **= 

# Equals (=)
a = 4                                   # value '4' is assigned in variable x
print(a)

# +=
b = 4
b += 5                                    # x += 5  --> x = x + 5
print(b)                                  #             x = 4 + 5 = 9

# -=
c = 4
c -= 1                                    # x -= 5  --> x = x - 1
print(c)                                  #             x = 4 - 1 = 3

# *=
d = 4
d *= 3                                    # x *= 5  --> x = x * 3
print(d)                                  #             x = 4 * 3 = 12

# /=
e = 4
e /= 2                                    # x /= 5  --> x = x / 2
print(e)                                  #             x = 4 / 2 = 2.0

# //=
f = 4
f //= 3                                   # x //= 3 --> x = x // 3
print(f)                                  #             x = 4 // 3 = 1

# **=
g = 4
g **= 3                                   # x **= 3 --> x = x ** 3
print(g)                                  #             x = 4 ** 3 = 64