# Arithmetic Operators --> Addition, Subtraction, Multiplication, Division 
#                      --> Floored Division, Modulus, Power

# Addition
print(5+6)

# Subtraction
print(8-2)

# Multiplication
print(6*3)

# Division
print(6/2)
print(14/4)                         # Automatically converts the answer in float



# Floored Division (// --> GIF) 
print(14//4)                        # Extract the integer value from a float number  
print(16//6)                        # 16/6 = 2.66666... --> 2

# Modulus (% --> Remaider)
print(10%3)
print(5%2)
print(4%2)

# Power/Double Star Operator (** --> Power of a number) 
print(2**3)                         # 2**3 =   2*2*2 =   8
print(5**4)                         # 5**4 = 5*5*5*5 = 625