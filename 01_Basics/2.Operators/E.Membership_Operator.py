# Membership Operator --> in
# States whether the I string is present in the II string

print("a" in "Akhilesh")                            # False          
print("A" in "Akhilesh")                            # True
print("k" in "Akhilesh")                            # True
print("kh" in "Akhilesh")                           # True
print("ki" in "Akhilesh")                           # False
print("ki" not in "Akhilesh")                       # True